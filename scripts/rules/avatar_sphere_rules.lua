function M.get_sphere_tokens(config, components)
    if not config or not components or not components.rating or not components.rating.tags then
        return {}
    end

    local rating_key = components.rating.tags[1]
    if rating_key ~= "AVATAR" then
        return {}
    end

    local sub_archetype = "UNKNOWN"
    
    if components.component_sub_archetype and 
       components.component_sub_archetype.sub_archetype and 
       #components.component_sub_archetype.sub_archetype > 0 then
        sub_archetype = components.component_sub_archetype.sub_archetype[1]
    end
 
    local sphere_list = SUB_ARCHETYPE_SPHERES[sub_archetype]
    
    if not sphere_list or #sphere_list == 0 then 
        return {}
    end
    
    return sphere_list
end

